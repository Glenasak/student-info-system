        private void btnListCheckActionPerformed(java.awt.event.ActionEvent evt) {
                String keyword = jTextField1.getText().trim();
                if (keyword.isEmpty()) {
                        loadAllScores();
                        return;
                }
                try {
                        List<Scores> list = Collections.emptyList();
                        boolean triedScoreId = false;

                        if (keyword.startsWith("#")) {
                                keyword = keyword.substring(1).trim();
                                triedScoreId = true;
                        }

                        try {
                                Integer numeric = Integer.valueOf(keyword);
                                Scores byId = controller.getById(numeric);
                                if (byId != null) {
                                        list = Collections.singletonList(byId);
                                } else {
                                        triedScoreId = true;
                                        list = controller.listByStudentId(numeric);
                                }
                        } catch (NumberFormatException e) {
                                list = controller.listByCourseName(keyword);
                        }

                        if ((list == null || list.isEmpty()) && !triedScoreId) {
                                try {
                                        Integer numeric = Integer.valueOf(keyword);
                                        Scores byId = controller.getById(numeric);
                                        if (byId != null) {
                                                list = Collections.singletonList(byId);
                                        }
                                } catch (NumberFormatException ignored) {
                                        // ignore, already handled above
                                }
                        }

                        if (list == null || list.isEmpty()) {
                                JOptionPane.showMessageDialog(this, "未找到匹配的成绩记录");
                                refreshTable(Collections.emptyList());
                        } else {
                                refreshTable(list);
                        }
                } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, "查询失败: " + ex.getMessage());
                }
        }

