        private Date parseExamDate(String raw) {
                if (raw == null || raw.trim().isEmpty()) {
                        return null;
                }
                try {
                        return dateFormat.parse(raw.trim());
                } catch (ParseException e) {
                        throw new IllegalArgumentException("考试日期格式应为 yyyy-MM-dd");
                }
        }

        private String formatExamDate(Date date) {
                return date == null ? "" : dateFormat.format(date);
        }

        private void applyExamDateForLatestScore(int studentId, String courseCode, double score, Date examDate) {
                if (examDate == null) {
                        return;
                }
                List<Scores> scores = controller.listAll();
                if (scores == null || scores.isEmpty()) {
                        return;
                }
                Scores target = null;
                for (Scores current : scores) {
                        if (current.getStudentId() == null || current.getScore() == null || current.getCourseCode() == null) {
                                continue;
                        }
                        if (!current.getStudentId().equals(studentId)) {
                                continue;
                        }
                        if (!current.getCourseCode().equalsIgnoreCase(courseCode)) {
                                continue;
                        }
                        if (Double.compare(score, current.getScore()) != 0) {
                                continue;
                        }
                        if (target == null || compareScoreId(current, target) > 0) {
                                target = current;
                        }
                }
                if (target != null) {
                        target.setExamDate(examDate);
                        controller.update(target);
                }
        }

        private void refreshTable(java.util.List<Scores> list) {
                DefaultTableModel model = (DefaultTableModel) TableScore.getModel();
                model.setRowCount(0);
                if (list == null || list.isEmpty()) return;
                List<Scores> sorted = new ArrayList<>(list);
                sorted.sort((a, b) -> compareScoreId(a, b));
                for (Scores s : sorted) {
                        model.addRow(new Object[]{
                                        s.getScoreId(),
                                        s.getStudentId(),
                                        s.getCourseCode() == null ? "" : s.getCourseCode(),
                                        s.getScore(),
                                        formatExamDate(s.getExamDate())
                        });
                }
        }

        private int compareScoreId(Scores a, Scores b) {
                Integer idA = a == null ? null : a.getScoreId();
                Integer idB = b == null ? null : b.getScoreId();
                if (idA == null && idB == null) {
                        return 0;
                }
                if (idA == null) {
                        return 1;
                }
                if (idB == null) {
                        return -1;
                }
                return Integer.compare(idA, idB);
        }

